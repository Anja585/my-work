# round.py
# This program takes in a float and outputs an integer, rounded up or down
# Author: Anja Antolkovic

# place the number here
floatNumber = float(input("Enter a float number: "))

# rounding the number the 0 decimal places
integerNumber = round(floatNumber)

# prints out the number
print("{} rounded is {}".format(floatNumber,integerNumber))
